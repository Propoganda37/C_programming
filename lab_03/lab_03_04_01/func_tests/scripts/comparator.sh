#!/bin/bash

# Проверяем обязательные аргументы на наличие
if [ $# -lt 2 ]; then
    exit 2
fi

# Проверяем, являются ли файлы существующими файлами
if [ ! -f "$1" ] || [ ! -f "$2" ]; then
    exit 2
fi

# Извлекаем последовательности целых чисел из обоих файлов
numbers1=$(grep -oE '(^|[[:space:]])[+-]?[0-9]+\b' "$1" | tr -d ' ')
numbers2=$(grep -oE '(^|[[:space:]])[+-]?[0-9]+\b' "$2" | tr -d ' ')

# Сравниваем последовательности
if [ "$numbers1" == "$numbers2" ]; then
    exit 0
else
    exit 1
fi
